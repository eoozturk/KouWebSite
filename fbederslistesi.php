					<center><h3>BİLGİSAYAR MÜHENDİSLİĞİ YÜKSEK LİSANS DERS LİSTESİ</h3></center>	
					<hr >
					<p class="text-center"><br>
				    <li><strong>Ağ Tasarım ve Yönetimi</strong></li>
				    <ul>
				      <li> Ağ temelleri ve guncel teknolojiler. Ag planlama, analiz, tasarim ve yonetim teknikleri. Ag yonetimi bilgi tabanlari ve protokolleri. Farkli ag teknolojileri, topoloji, performance ve gercek hayat uygulamalari acisindan aciklanip karsilastirilacak. Ag yonetim sistemleri hata, konfigurasyon, guvenlik ve performansida icerir sekilde incelenecektir.</li>
				      
					<br>
			      </ul>
				    <li><strong>Algoritmaların tasarım ve analizi</strong></li>
				    <ul>
				      	<li>Algoritmaların temel kavramları. matematiksel gereksinimler. Rekürsif eşitlikler ve eşitsizlikler. Karmaşıklık ölçütleri, O, o ve diğer notasyonlar. Çeşitli alanlardaki klasik algoritmaların karmaşıklık analizi. Sıralama, arama, çizgeler, eşleme, matris işlemleri, dönüşümler</li>
			          	
				    </ul>
			      </ul>
			      <br>
		
			
				    <li><strong>Ayrık Sistemler için İleri Olasılık</strong></li>
				    <ul>
				      <li>Olasılık uzayı, rastgele değişkenler, beklendik değer, koşullu beklendik değer, stokastik yakınsama, karakteristik fonksiyonlar, limit teoremleri, Markov ve Gauss Süreçlerine Giriş, Durağan Süreçler, Spektrak Gösterim, Ergodiklik, Yenilenen Süreçler, Martingale ve Kestirime Uygulaması, Tahmin, ve Kuyruk Teorisi</li>
			           </ul>
			            <br>
				  
				    <li><strong>Bilgisayar Ağları ve Haberleşmesi</strong></li>
				    <ul>
				      <li>Veri ve bilgisayar haberleşmesi temelleri; OSI temel referans modeli; fiziksel, veri bağlantı, ağ, ve ulaşım katmanları; yönlendirme akış kontrolü, tıkanıklık kontrolü; ağlar arası ağ; TCP/IP protokolleri; üst katman protokolleri; kablosuz ağlara giriş.</li>
			           </ul>
				<br>
				    <li><strong>Bilgisayar Mühendisliğinde Özel Konular</strong></li>
				    <ul>
				      	<li>Konular dersin sunulduğu döneme göre değişebilir.Konular bilgisayar mühendisliğindeki teknolojik veya teorik gelişmeler arasından seçilecektir.</li>
			          	
				    </ul>
			     
			
				  <br>
				    <li><strong>Dağıtık Sistemler</strong></li>
				    <ul>
				      <li>Dağıtık sistemler algoritmaların temeli, problemler, hata toleransı göz önüne alınarakdağıtık uygulamalar dizayn etmek ve anlamak için gerekli modeller ve yöntemler. Web Servisler.</li>
			        </ul><br>
				    <li><strong>İnsan Bilgisayar Etkileşimi</strong></li>
				    <ul>
				      <li>Bu dersin amacı öğrencilere insan bilgisayar etkileşimi (human computer interaction) ve kullanıcı merkezli tasarım konularını; insan bilgisayar etkileşimi temel prensipleri, etkileşimli sistemler genel tasarım ilkeleri, kullanılabilirlik testleri (temel prensipler ve kullanıcı testleri) kavramları ile tanıtmaktır. </li>
			           </ul><br>
				   <li><strong>İnternet Madenciliği</strong></li>
				    <ul>
				      <li>İnternet madenciliğine giriş; Temel www teknolojileri; Veri ön işleme teknikleri; İnternet kullanım madenciliği; İnternet erişim kayıtları; İnternet öneri sistemleri; Kullanıcı profili öğrenme ve kişiselleştirme; İnternet içerik madenciliği; Metin kümeleme ve analizi; İnternet yapı madenciliği; Bağ analizi; İnternet metinleri; İnternette arama ve örümcek teknikleri.</li>
			           </ul><br>
				    <li><strong>Kablosuz Haberleşme ve Gezgin Sistemler</strong></li>
			      	<br>
				    <li><strong>Makine Öğrenmesi</strong></li>
			      	<ul>
					<li>Giriş; Eğiticili öğrenme; Yapay sinir ağları; Destek vektör sistemleri; Öğrenme algoritmalarının değerlendirilmesi; Bayes teorisi; Karar ağaçları; Parametrik yöntemler; Çok değişkenli yöntemler; Boyut indirgeme; Kümeleme; Güncel uygulamalar.</li>
			      	</ul><br>
				    <li><strong>Mobil Uygulama Geliştirme</strong></li>
			      	<ul>
					<li>Kestirim teorisine giriş. Matematiksel operasyonlar. Matris diferansiyel hesabı. En küçük kareler hesabı. Ardışıl Kestirim. Rasgele değişkenler. Bazı dağılım fonksiyonları. Durağan prosesler. Wiener filtre problemi. Spektral faktorizasyon. Rasgele girişli sistemler. Optimum durum kestirimi problemi. Kalman filtresi ve kararlılığı. Sürekli hal durumu. Renkli gürültü için durum vektörü. Lineer ayrık zamanlı rasgele sistemler. Kalman filtrelerinin tipleri. Artırılmış(extended) Kalman filtresi.</li>
			      	</ul><br>
				    <li><strong>Special Topics in Human Computer Interaction</strong></li>
			      	<br>
				    <li><strong>Tasarım Örüntüleri</strong></li>
			      	<ul>
					<li> Dersin amacı nesneye yönelik yazılım geliştirme sürecinde analiz ve tasarım aşamalarında öğrencilerin UML modelleme dilini kullanarak gereksinim analizi ve tasarım örüntülerini kullanarak yeniden kullanılabilir yazılım tasarımları gerçekleştirmelerini sağlamaktır.</li>
			      	</ul><br>
				    <li><strong>User Experience Design</strong></li>
			      	<br>
				    <li><strong>Veri Madenciliği</strong></li>
			      	<ul>
					<li>Veri madenciliğinin önemi.Temel veri madenciliği teknikleri.Karar ağaçları.Birliktelik kuralları.K-means algoritması.Genetik öğrenme.Veritabanlarında bilgi keşfi.Veri ambarları.Gelişmiş veri madenciliği teknikleri.Veri madenciliği uygulama alanları.</li>
			      	</ul><br>
				    <li><strong>Veri ve Ağ Güvenliği</strong></li>
			      	<ul>
					<li>Şifreleme Sistemleri ve Sistemlerine Giriş, Açık Anahtarlı Şifreleme ve RSA Şifreleme Sistemleri, DES(Data Encryption Standart) Şifreleme Sistemi, Veri ve Ağ Güvenliğine Giriş, Güvenlik Politikaları, Güvenlik Duvarı (Fire Wall), AAA (Authentication-Authorization -Acounting), ACL (Access Control List) ve NAT(Ağ Adres Dönüşümü), VPN (Virtual Private Network), Saldırı Tespit Sistemleri, E-Ticaretin Güvenliği ve SSL, ECC (Eliptic Curve Cryptosystem), Sayısal İmza, IP Güvenliği.</li>
			      	</ul><br>
				    <li><strong>Veritabanı Sistemleri</strong></li>
			      	<ul>
					<li>Temel tanımlar ve kavramlar. Er modeli ile veri modelleme. Varlık, Varlık Kümesi, Nitelik Kavramları. Bağıntı,Bağıntı Tipleri, Roller, Yapısal Kısıtlar. Genelleme. Kümeleme. Anahtarlar. İlişkisel veri modeli. İlişkisel cebir. Sql ilişkisel veritabanı standardı. Fonksiyonel bağımlılıklar ve normalizasyon.</li>
			      	</ul><br>
				    <li><strong>Yapay Zeka ve Bilişsel Sistemler</strong></li>
			      	<ul>
					<li>Yapay zekaya giriş. Programlama. Dizi, ağaç, küme, kuyruk ve tablo yapıları.Bilgi gösterimi: Üretim kuralları, içerme hiyeyarşileri, önermesel ve yargısal hesap, çıkarım kuralları, çerçeveler, anlamsal ağlar, kısaltmalar ve dizgesel yaklaşımlar. Arama: Hipotez ve test etme, derinlik öncelikli arama, genişlik öncelikli arama, sezgisel arama, optimal arama, oyun ağaçları ve dönüşüklü arama, minimax arama, alpha-beta indirgeme. Öğrenme: Betimleme ağaçları, yapay sinir ağları, perceptronlar, genetik algoritmalar, uzman sistemler, doğal dil işlem, konuşma tanıma, bilgisayarla görü, Bilişsel sistemlere giriş, bilişsel yapay zeka.</li>
			      	</ul><br>
				    <li><strong>Yazılım Kalite Yönetimi</strong></li>
			      	<ul>
					<li>Bu dersin amacı yazılım kalitesi, yazılım kalite güvencesi ve yazılım süreç iyileştirme ile ilgili temel kavram ve prensipleri tanımlamak ve kullanılacak teknik ve yöntemleri nin yazılım geliştirme sürecinde uygulanabilmesini sağlamaktır</li>
			      	</ul>
				  
